function formatArray(arr:number[]){
    let result = '';
      let start = arr[0];
      let end = arr[0];
      for (let i = 1; i < arr.length; i++) {
        if (arr[i] - arr[i - 1] === 1) {
          end = arr[i];
        } else {
          if (start === end) {
            result += `${start}, `;
          } else {
            result += `${start}-${end}, `;
          }
          start = arr[i];
          end = arr[i];
        }
      }
      if (start === end) {
        result += start;
      } else {
        result += `${start}-${end}`;
      }
      return result;
}

